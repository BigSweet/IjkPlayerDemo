    //显示时间
    private static final int SYNC_DURATION = 1;
    @SuppressWarnings("HandlerLeak")
    private Handler mHandler = new Handler(Looper.getMainLooper()) {
        @Override
        public void handleMessage(Message msg) {
            switch (msg.what) {
                /**滑动中，同步播放进度*/
                case SYNC_DURATION:
                    long pos = mMediaPlayer.getCurrentPosition();
                    long position = mMediaPlayer.getCurrentPosition();
                    long duration = mMediaPlayer.getDuration();
                    long seekBarCurrentPosition = 0;
                    if (duration > 0) {
                        seekBarCurrentPosition = 100L * position / duration;
                    }
                    int percent = mCurrentBufferPercentage * 10;
                    msg = obtainMessage(SYNC_DURATION);
                    listener.durationUpdate(generateTime(position), generateTime(duration), seekBarCurrentPosition, percent);
                    sendMessageDelayed(msg, 1000 - (pos % 1000));
                    break;
            }
        }
    };


